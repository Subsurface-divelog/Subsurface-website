<p>Ekipa Subsurface z przyjemnością ogłasza udostępnienie kolejnej wersji Subsurface. Wersja 4.2 to poprawki błędów i ulepszenia w stosunku od 4.1.
Najważniejsze zmiany to:</p>
<ul>
<li>Instalator Windows jest teraz 64 bitowy i zawiera Qt5</li>
<li>dalej dostępny jest instalator 32 bitowy z Qt4 ale ta wersja zawiera kilka znanych problemów (między innymi kilka błędów z drukowaniem poprawionych w Qt5</li>
<li>Instalator dla Mac również zaktualizowany do Qt5.</li>
<li>Aktualizacja do najnowszej wersji libdivecomputer dla Mac i Windows</li>
<li>Planer nurkowań powrócił - tym razem z edytorem graficznym, wsparciem dla rożnych prędkości wynurzania, przerwami powietrznymi podczas deco i wieloma innymi ustawieniami.  Gotowy plan nurkowania można tez wydrukować (w postaci tekstowej do umieszczenia na tabliczce). Jeden mankament to to, że obecnie nie można powrócić do trybu planowania, co oznacza, że po zapisaniu planu nie można go już dalej edytować i trzeba zacząć nowe planowanie od początku. To zostanie niedługo zmienione.</li>
<li>Można dodawać zdjęcia do profilu nurkowania</li>
<li>Znacznie ulepszono drukowanie (renderowanie wykresów, prezentacja danych), dodano tryb "1 nurkowanie na stronę"</li>
<li>Wprowadzanie danych do nurkowania jest teraz bardziej intuicyjne i spójne</li>
<li>Dodano moduł eksportu logbooka do HTML - to na razie wstępna wersja, ale działa już całkiem nieźle</li>
<li>Dodano wsparcie do importowania danych z komputerów Seabear</li>
<li>Dodano ankietę, dzięki które możemy lepiej poznać potrzeby użytkowników</li>
<li>Poprawienie i uzupełnienie instrukcji obsługi</li>
<li>Instrukcja obsługi dostępna teraz także w rosyjskim i hiszpańskim</li>
</ul>
<p>Jeśli używasz Subsurface 4.1 na Windows lub Mac możesz sprawdzić dostępność aktualizacji z aplikacji w menu Pomoc. W przeciwnym wypadku możesz pobrać instalator z <a href="https://subsurface-divelog.org/pl/download/">naszej strony. Jeśli używasz 32 bitowej wersji Windows to zwróć uwagę, że domyślny instalator jest 64 bitowy.</a></p>