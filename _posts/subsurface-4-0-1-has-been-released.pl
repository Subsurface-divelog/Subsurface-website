Ekipa Subsurface z przyjemnością ogłasza udostępnienie najnowszej wersji Subsurface.

Jest to uaktualnienie mające na celu przede wszystkim poprawki błędów zauważonych w wersji 4.0

Zmiany w wersji  4.0.1 w stosunku do Subsurface 4.0:
<ul>
	<li>poprawiono kilka błędów związanych z obsługą znaków narodowych w opisie nurkowań, profilu i nazwach plików w Windows</li>
	<li>dodano możliwość generowania logów diagnostycznych z biblioteki libdivecomputer</li>
	<li>liczne poprawki w importowaniu/ eksportowaniu plików z/do innych formatów</li>
	<li>arkusze stylów XLTS są wbudowane, co eliminuje problem z ich znajdowaniem</li>
	<li>wsparcie dla większej liczby formatów współrzędnych GPS</li>
	<li>poprawiono kilka błędów związanych z edytowaniem nurkowań wprowadzonych ręcznie </li>
	<li>poprawiono kilka błędów związanych z edytowaniem butli i balastów</li>
	<li>zwiększono liczbę możliwych rodzajów balastów podczas jednego nurkowania do 6</li>
	<li>naprawiono wybieranie rodzaju czcionki używanej przez program </li>
	<li>lepszy wygląd przy używaniu ciemnych motywów ekranu</li>
	<li>dodano wersję programu działającą ze starszymi wersjami Mac OS/X (&gt;= 10.5)</li>
</ul>