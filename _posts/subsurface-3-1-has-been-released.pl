Ekipa Subsurface z przyjemnością ogłasza udostępnienie najnowszej wersji Subsurface.

Nowości w tej wersji (w porównaniu do Subsurface 3.0.2):
<ul>
	<li>dodawanie tagi dla nurkowań (na przykład 'z łodzi', 'wrakowe', 'głębokie', itp)</li>
	<li>filtrowanie nurkowań po tagach</li>
	<li>możliwość modyfikacji wydarzeń w profilu nurkowania dostępna z poziomu menu kontekstowego (w tym możliwość dodania zmiany gazu)</li>
	<li>eksport nurkowań do logbooka online - divelogs.de</li>
	<li>import plików Suunto DM4</li>
	<li>opcje formatowania wydruku</li>
	<li>drukowanie profili w kolorze</li>
	<li>planowanie nurkowań: wybór głębokości ostatniego przystanku deco - 3m lub 6m</li>
	<li>wsparcie dla Heinrichs Weikamp OSTC3 i Suunto DX</li>
	<li>znaczne przyspieszenie pobieranie danych z Shearwater Petrel</li>
	<li>różne poprawki</li>
</ul>
